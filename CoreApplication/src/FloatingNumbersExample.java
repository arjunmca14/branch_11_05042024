
public class FloatingNumbersExample {
	public static void main(String[] args) {
		float f=10;
		System.out.println(f);
		
		float f1=10.0f;
		float f2=123.4572357f;
		System.out.println("f2 value>>"+f2);
//		=================================
		
		double d1=10;
		double d=10.0;
		
		double d2=123.4572357D;
		System.out.println("d2 value>>>"+d2);
		
	}

}
